/*
Data Types

int 
double
string
bool
dynamic
*/
int main() {
  int amount1 = 100;
  var amount2 = 200;
  print("$amount1 | $amount2");

  double damount1 = 100.00;
  var damount2 = 200.00;
  print("$damount1 | $damount2");

  String name1 = "Srijan";
  var name2 = "Singh";
  print("$name1 | $name2");

  bool data1 = true;
  var data2 = false;
  print("$data1 | $data2");

  dynamic dynamicVariable = 11;
  print(dynamicVariable);

  dynamicVariable = "Dart";
  print(dynamicVariable);

  return 0;
}
